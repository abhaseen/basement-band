    void pressPad(String drumPath){
        
        //plays sound files
        try{
                AudioInputStream audioInputStreamED = AudioSystem.getAudioInputStream(new File(drumPath));
                Clip clipED = AudioSystem.getClip();
                clipED.open(audioInputStreamED);
                clipED.start();
            }
            
        catch (Exception ex){
                System.out.println("Error occured: pad could not be played. " + ex);
            }
    }
    
