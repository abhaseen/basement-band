    void pressKey(String pianoPath, String steelDrumPath){
        
        //The set of sounds used will differ based on what preset is selected
        if (jEPianoCheck.isSelected()){
            try{
                AudioInputStream audioInputStream = AudioSystem.getAudioInputStream(new File(pianoPath));
                Clip clip = AudioSystem.getClip();
                clip.open(audioInputStream);
                clip.start();
            }
            
            catch (Exception ex){
                System.out.println("Error occured: key could not be played. " + ex);
            }
        }
        
        if (jSteelDrumCheck.isSelected()){
            try{
                AudioInputStream audioInputStreamSD = AudioSystem.getAudioInputStream(new File(steelDrumPath));
                Clip clipSD = AudioSystem.getClip();
                clipSD.open(audioInputStreamSD);
                clipSD.start();
            }
            
            catch (Exception ex){
                System.out.println("Error occured: key could not be played.");
            }
        }
        
    }
       
