    /**
     * 
     * @param path
     * @param button
     * @param size_width
     * @param size_height 
     */
    void setImage(String path, JButton button, int size_width, int size_height){
        
        //sets the image icon of the button (resizes the image to fit the button)
        ImageIcon icon = new ImageIcon(path);
        Image img = icon.getImage();  
        Image newimg = img.getScaledInstance( size_width, size_height,  java.awt.Image.SCALE_SMOOTH );  
        icon = new ImageIcon( newimg );
        button.setIcon(icon);
        validate();
        
    }
    
    /**
     * 
     * @param soundPath 
     */
    void pressButton(String soundPath){
        
        //plays the sound file
        try{
                AudioInputStream audioInputStreamED = AudioSystem.getAudioInputStream(new File(soundPath));
                Clip clipED = AudioSystem.getClip();
                clipED.open(audioInputStreamED);
                clipED.start();
            }
            
        catch (Exception ex){
                System.out.println("Error occured: drum button could not be played. " + ex);
            }
        
    }
    
